#!/bin/bash
# 7-тапсырма: Ағындар (потоктар) және қайтару кодтарын (код возврата) көрсететін скрипт

echo "--- Стандартты ағындарды көрсету ---"

# 1. Стандартты шығаруды (stdout) файлға бағыттау
echo "Бұл сәтті хабарлама" > success.log
echo "stdout (шығару ағыны) success.log файлына жазылды"

# 2. Қателер ағынын (stderr) бағыттау
# Жоқ файлды немесе буманы оқуға тырысамыз
ls /nonexistent_directory 2> error.log
echo "stderr (қате) ұсталып, error.log файлына жазылды"

# 3. Қайтару кодын өңдеу ($?)
# Әдейі қате команда орындаймыз
ping -c 1 invalid.host.domain > /dev/null 2>&1

if [ $? -eq 0 ]; then
    echo "Команда сәтті орындалды (Код 0)"
else
    echo "НАЗАР АУДАРЫҢЫЗ: Команда қатемен аяқталды! (Қайтару коды: $?)"
fi